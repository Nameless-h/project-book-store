package DTO;

public class chucnang {
    String ma, ten;

    public chucnang() {
    }

    public chucnang(String ma, String ten) {
        this.ma = ma;
        this.ten = ten;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getMa() {
        return ma;
    }

    public void setMa(String ma) {
        this.ma = ma;
    }
}
